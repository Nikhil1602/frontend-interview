// There were mainly three types of error in Javascript:

// 1. Syntax Error
// 2. Reference Error
// 3. Type Error

// Syntax Error: When code structure is invalid and javascript
// cannot parse it, we call it syntax error.

// E.g.
//
// let a = ;
// if (true {
//    console.log("Hi");
// }
//

// Reference Error: When javascript is trying to access a variable
// that does'nt exist, we call it reference error.

// E.g.
//
// console.log(a);
// let a = 5;

// Type Error: When a variable exists but it's used with wrong
// type or operation, we call it type error.

// E.g.
//
// let num = 10;
// num.toUpperCase();