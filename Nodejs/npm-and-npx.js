// npm is used to install and manage packages, while npx is
// used to execute packages directly without installing
// them globally.

// npm (Node Package Manager):
// - Used to install packages and manage dependencies.
// - Packages are installed globally or locally in a project.
// - Example: npm install express

// npx (Node Package Executor):
// - Used to execute packages without installing them globally.
// - It runs the package from the local node_modules or fetches it from the registry if not found.
// - Example: npx create-react-app my-app