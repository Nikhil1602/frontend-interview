// List of numeric data types

// Integers (whole numbers):
// - INT / INTEGER ---> standar integers
// - SMALLINT ---> smaller range
// - BIGINT ---> very large numbers

// Decimal (exact precision):
// - DECIMAL(p, s) ---> fixed precision (e.g. money)
// - NUMERIC(p, s) ---> same as DECIMAL

// Floating-point (approximate):
// - FLOAT ---> approximate decimal values
// - REAL, DOUBLE ---> different precision levels

